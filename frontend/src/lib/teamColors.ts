/** Returns the primary hex color for a team, or null if unknown. */
export function getTeamColor(teamName: string): string | null {
  if (!teamName) return null;
  // Exact match
  if (TEAM_COLORS[teamName]) return TEAM_COLORS[teamName];
  // Partial match (e.g. "Manchester City FC" contains "Manchester City")
  for (const [key, color] of Object.entries(TEAM_COLORS)) {
    if (teamName.includes(key) || key.includes(teamName)) return color;
  }
  return null;
}
